package com.youzheng.zhejiang.financebooking.Model.home;

public class MVersionDataEntity {
    private int id ;
    private String versionNo ;
    private String versionUrl ;
    private String versionFname ;
    private String versionTime ;
    private String versionTerminal ;
    private int versionStatus ;
    private int versionUpdate ;
    private int versionToUpdate ;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getVersionNo() {
        return versionNo;
    }

    public void setVersionNo(String versionNo) {
        this.versionNo = versionNo;
    }

    public String getVersionUrl() {
        return versionUrl;
    }

    public void setVersionUrl(String versionUrl) {
        this.versionUrl = versionUrl;
    }

    public String getVersionFname() {
        return versionFname;
    }

    public void setVersionFname(String versionFname) {
        this.versionFname = versionFname;
    }

    public String getVersionTime() {
        return versionTime;
    }

    public void setVersionTime(String versionTime) {
        this.versionTime = versionTime;
    }

    public String getVersionTerminal() {
        return versionTerminal;
    }

    public void setVersionTerminal(String versionTerminal) {
        this.versionTerminal = versionTerminal;
    }

    public int getVersionStatus() {
        return versionStatus;
    }

    public void setVersionStatus(int versionStatus) {
        this.versionStatus = versionStatus;
    }

    public int getVersionUpdate() {
        return versionUpdate;
    }

    public void setVersionUpdate(int versionUpdate) {
        this.versionUpdate = versionUpdate;
    }

    public int getVersionToUpdate() {
        return versionToUpdate;
    }

    public void setVersionToUpdate(int versionToUpdate) {
        this.versionToUpdate = versionToUpdate;
    }
}
